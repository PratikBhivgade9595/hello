package com.bridgelabz;

public class Swapping {
    public static void main(String[] args) {
        int i = 10;
        int i1 = 20;
        System.out.println("i="+i+" and i1="+i1);

        int temp = i;
        i = i1;
        i1 = temp;
        System.out.println("i="+i+" and i1="+i1);

    }
}
